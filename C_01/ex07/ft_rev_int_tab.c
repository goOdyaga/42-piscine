void	ft_rev_int_tab(int *str, int size)
{
	int	i;
	int	a;

	i = 0;
	while (i < (size / 2))
	{
		a = str[5 - i - 1];
		str[5 - i - 1] = str[i];
		str[i] = a;
		i++;
	}
}
